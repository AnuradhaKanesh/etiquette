public class Calculator {

    public int getAdditionResult(int num1, int num2) {
        return num1 + num2;
    }
    public int getMultiplicationResult(int num1, int num2) {
        return num1 * num2;
    }
    public int getSubtractionResult(int num1, int num2) {
        return num1 - num2;
    }
    public int getDivisionResult(int num1, int num2) {
        return num1 / num2;
    }

}
